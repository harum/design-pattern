export default class GarageDoor {
  up() {
    console.log('Garage door is open');
  }

  close() {
    console.log('Garage door is close');
  }

  stop() {
    console.log('Garage door stop');
  }

  lightOn() {
    console.log("Garage's light is on");
  }

  lightOff() {
    console.log("Garage's light is off");
  }
}
